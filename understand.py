def explain_alignment():
    header("HOW THE 4 FILES CONNECT  —  Tidy Data & Key Mappings")

    print(f"""
  ┌─────────────────────┬────────────────────────────────────────────────────┐
  │  Condition/Modality │  File pattern                                      │
  ├─────────────────────┼────────────────────────────────────────────────────┤
  │  Single / Lab       │  <N>_visual_search_*.csv          (N = participant) │
  │  Single / Phone     │  <N>_attentional_spotter_results.csv               │
  │  Multiple / Lab     │  <N>_visual_search_*.csv          (N = participant) │
  │  Multiple / Phone   │  <N>_attentional_spotter_results.csv               │
  └─────────────────────┴────────────────────────────────────────────────────┘

  Participant linking:
    Lab  participant col  →  e.g. "sahithi", "1", "22"
    Phone PlayerID col   →  e.g. "Player_2", "Player_22"
    ⚠ You need a lookup table to match these reliably across all 37 participants.
""")

    section("DV ALIGNMENT TABLE  —  same concept, different column name per file")

    print(f"""
  Concept               │ Single/Lab           │ Multiple/Lab          │ Phone (both)
  ──────────────────────┼──────────────────────┼───────────────────────┼──────────────────────────
  Initial RT (ms)       │ mouse.time[0] × 1000 │ click_times[0] × 1000 │ InitialResponseTime(ms)
  Inter-response time   │  N/A (1 target)      │ diff(click_times)×1000│ AvgInterTargetTime(ms)
  Hit (accuracy)        │ 'target' in          │ len(clicked) / 5      │ HitRate(%) / 100
                        │  mouse.clicked_name  │                       │
  False alarm           │ non-target name in   │ N/A per trial         │ FalseAlarms (count)
                        │  mouse.clicked_name  │                       │
  Success (penalised)   │  compute manually    │  compute manually     │ SuccessRate(%)
  Trial/level #         │ trials.thisN         │ trials.thisN          │ Level
  Trial duration        │ trial.stopped −      │ trial.stopped −       │  not directly stored
                        │  trial.started       │  trial.started        │  (use timestamps)
  Participant ID        │ participant col       │ participant col       │ PlayerID col
""")

    section("BETWEEN-SUBJECTS IV  (Target Load)")
    print("""
  Participants 1–21  →  SINGLE TARGET group
      Lab file:  1_visual_search_*.csv   |  Phone file: 1_attentional_spotter_results.csv
      Each trial / level has exactly 1 target hidden among distractors.

  Participants 22–37  →  MULTIPLE TARGET group  (16 participants per brief)
      Lab file:  22_visual_search_*.csv  |  Phone file: 22_attentional_spotter_results.csv
      Each trial / level has 5 targets (lab) or growing N targets (phone game).
""")

    section("WITHIN-SUBJECTS IV  (Modality)")
    print("""
  Every participant did BOTH:
    • Lab task  (PsychoPy on a computer, controlled environment)
    • Phone game (mobile app, gamified interface)

  Counterbalancing: half did Lab first, half did Game first
    → tracked via the 'session' column in lab files (session 1 vs 2).
""")

    section("KEY ANALYTICAL NOTE — Unit of Analysis mismatch")
    print("""
  Lab  → 15 TRIALS per participant
  Phone → 15–20 LEVELS per participant (and levels can be retried)

  Before running ANOVA / t-tests you must AGGREGATE to one score per participant:
    Lab:   mean(RT_ms)  and  mean(hit) across all trials
    Phone: mean(InitialResponseTime(ms)) across completed levels

  Then you have one row per participant per modality (the standard mixed ANOVA format).
""")


